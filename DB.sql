-- 백 발생한 에러 적재
CREATE SEQUENCE SEQ_TKW_BACK_LOG START 1;
CREATE TABLE TKW_BACK_LOG
(
    BACK_LOG_ID INTEGER DEFAULT NEXTVAL('SEQ_TKW_BACK_LOG') NOT NULL PRIMARY KEY,
    BACK_LOG_MESSAGE TEXT,
    BACK_LOG_OCCUR_TIME timestamp with time zone default now()
);
COMMENT ON COLUMN TKW_BACK_LOG.BACK_LOG_SEQUENCE IS 'Backend 로그 자동 생성 ID';
COMMENT ON COLUMN TKW_BACK_LOG.BACK_LOG_MESSAGE IS 'Backend 에러 로그 메시지';
COMMENT ON COLUMN TKW_BACK_LOG.BACK_LOG_OCCUR_TIME IS 'Backend 에러 로그 발생 시간';

